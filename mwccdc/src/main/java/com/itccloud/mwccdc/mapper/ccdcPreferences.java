package com.itccloud.mwccdc.mapper;

public class ccdcPreferences {
	
	private String standposition;
	private Integer numberseats;
	private Integer numberpreferred;
	private Float discountprice;
	private Float totalearnings;
	
	public String getStandposition() {
		return standposition;
	}
	public void setStandposition(String standposition) {
		this.standposition = standposition;
	}
	public Integer getNumberseats() {
		return numberseats;
	}
	public void setNumberseats(Integer numberseats) {
		this.numberseats = numberseats;
	}
	public Integer getNumberpreferred() {
		return numberpreferred;
	}
	public void setNumberpreferred(Integer numberpreferred) {
		this.numberpreferred = numberpreferred;
	}
	public Float getDiscountprice() {
		return discountprice;
	}
	public void setDiscountprice(Float discountprice) {
		this.discountprice = discountprice;
	}
	public Float getTotalearnings() {
		return totalearnings;
	}
	public void setTotalearnings(Float totalearnings) {
		this.totalearnings = totalearnings;
	}
	

}
